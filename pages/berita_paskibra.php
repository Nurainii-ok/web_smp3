            <p>Pada hari Sabtu (7/9) PASKIBRA BINTARALOKA kembali pulang membawa 3 Piala 
                pada ajang LPBB Singhasari tingkat provinsi yang dilakukan di SMPN 21 Malang.<br>

                LPBB sendiri adalah Lomba Pasukan Baris Berbaris yang dilakukan oleh PASKIBRA 
                dari jenjang SD/MI, SMP/MTs, SMA/SMK/MA sederajat.<br>

                Perlu latihan khusus dan pastinya berminggu minggu untuk mendapatkan Piala dari event kali ini, 
                tentunya yang dilakukan setiap hari dari jam pulang sekolah hingga 17:00 WIB.<br>

                Kali ini tim PASKIBRA mengeluarkan 2 tim, yaitu tim A yang tampil dengan nomor urut 11, 
                dan tim B tampil dengan nomor urut 24. PASKIBRA BINTARALOKA mengeluarkan 2 tim untuk memaksimalkan 
                potensi seluruh anggota.<br>

                Pidato yang disampaikan danton tim A –Athallahsyahdan Putra, mencerminkan tentang Malin Kundang, 
                sedangkan pidato yang disampaikan danton tim B –Dzakiyy Fadhil Afif, mencerminkan tentang sejarah 
                “Perobekan Bendera Belanda Di Surabaya”.<br>

                Fattah Rafi mengatakan, ada satu insiden kecil kala penampil nomor urut 23 menggunakan tepung dan 
                mereka tidak membersihkan, sehingga berimbas ke anggota tim KRADIKA, yaitu Nayshela terpeleset 
                saat melakukan penjuruan, sontak pun penonton menjadi ricuh akibat jatuhnya Nayshela.<br>

                Namun, itu tidak menjadi halangan untuk tim PASKIBRA mendapatkan juara, mereka mendapatkan 3 piala, 
                yang pertama piala Caraka 3, Harapan 3, dan best like tiktok, yang pasti disumbangkan untuk sekolah.<br>

                “Semoga untuk event selanjutnya, tim PASKIBRA dapat lebih baik lagi dan mendapatkan 
                juara umum” ujar Rafi (7/9/2024).<br>
